import type { VarsFile, Variable } from "./types.js";

export function generateTypes(varsFile: VarsFile, envFile = ".vars"): string {
  const lines: string[] = [];

  lines.push("// vars.generated.ts (auto-generated by vars — do not edit)");
  lines.push('import { z } from "zod";');
  lines.push('import { Redacted } from "@vars/core";');
  lines.push("");

  // env-aware boolean coercion (handles "false", "0", "" correctly)
  lines.push("const FALSY = new Set([\"false\", \"0\", \"\"]);");
  lines.push("const envBoolean = () =>");
  lines.push("  z.preprocess(");
  lines.push("    (v) => (typeof v === \"string\" ? !FALSY.has((v as string).toLowerCase().trim()) : Boolean(v)),");
  lines.push("    z.boolean(),");
  lines.push("  );");
  lines.push("");

  // Schema object
  lines.push("export const schema = z.object({");
  for (const v of varsFile.variables) {
    const schemaExpr = v.schema.includes("z.coerce.boolean()")
      ? v.schema.replace("z.coerce.boolean()", "envBoolean()")
      : v.schema;
    lines.push(`  ${v.name}: ${schemaExpr},`);
  }
  lines.push("});");
  lines.push("");

  // Env type — use z.infer but wrap string fields with Redacted
  lines.push("export type Env = {");
  for (const v of varsFile.variables) {
    const optional = v.schema.includes(".optional()");
    const tsType = inferTsType(v);
    const key = optional ? `${v.name}?` : v.name;
    lines.push(`  ${key}: ${tsType};`);
  }
  lines.push("};");
  lines.push("");

  // Parse helper — validates + wraps strings in Redacted
  lines.push("function parseEnv(input: Record<string, unknown>): Env {");
  lines.push("  const parsed = schema.parse(input);");
  lines.push("  return {");
  for (const v of varsFile.variables) {
    if (isStringType(v)) {
      lines.push(`    ${v.name}: new Redacted(parsed.${v.name}),`);
    } else {
      lines.push(`    ${v.name}: parsed.${v.name},`);
    }
  }
  lines.push("  } as Env;");
  lines.push("}");
  lines.push("");

  // Server env export
  lines.push("export const env: Env = parseEnv(process.env);");
  lines.push("");

  // Client env export (PUBLIC_* and NEXT_PUBLIC_* variables only)
  const clientVars = varsFile.variables.filter(
    (v) => v.name.startsWith("PUBLIC_") || v.name.startsWith("NEXT_PUBLIC_"),
  );
  if (clientVars.length > 0) {
    const pickKeys = clientVars.map((v) => `"${v.name}"`).join(" | ");
    lines.push(`export const clientSchema = schema.pick({`);
    for (const v of clientVars) {
      lines.push(`  ${v.name}: true,`);
    }
    lines.push("});");
    lines.push(`export type ClientEnv = Pick<Env, ${pickKeys}>;`);
    lines.push("export const clientEnv: ClientEnv = parseEnv(process.env) as unknown as ClientEnv;");
    lines.push("");
  }

  return lines.join("\n");
}

function inferTsType(variable: Variable): string {
  const schema = variable.schema;

  // Number types
  if (schema.includes("z.number()") || schema.includes("z.coerce.number()")) {
    return "number";
  }

  // Boolean types
  if (schema.includes("z.boolean()") || schema.includes("z.coerce.boolean()")) {
    return "boolean";
  }

  // Enum types — extract literal union
  const enumMatch = schema.match(/z\.enum\(\[([^\]]+)\]\)/);
  if (enumMatch) {
    return enumMatch[1]
      .split(",")
      .map((s) => s.trim())
      .join(" | ");
  }

  // String types — wrapped in Redacted
  return "Redacted<string>";
}

function isStringType(variable: Variable): boolean {
  const schema = variable.schema;
  if (schema.includes("z.number()") || schema.includes("z.coerce.number()")) return false;
  if (schema.includes("z.boolean()") || schema.includes("z.coerce.boolean()")) return false;
  // Enums are string-based, wrap them too
  return true;
}
