import type { VarsFile, Variable } from "./types.js";

export function generateTypes(varsFile: VarsFile, envFile = ".vars"): string {
  const lines: string[] = [];

  lines.push("// env.generated.ts (auto-generated by vars — do not edit)");
  lines.push("// Use with a framework plugin (withEnvx, varsPlugin, etc.) that");
  lines.push("// loads .vars into process.env at build time.");
  lines.push("");

  // Env type
  lines.push("export type Env = {");
  for (const v of varsFile.variables) {
    const optional = v.schema.includes(".optional()");
    const tsType = inferTsType(v);
    const key = optional ? `${v.name}?` : v.name;
    lines.push(`  ${key}: ${tsType};`);
  }
  lines.push("};");
  lines.push("");

  // Helper to read from process.env with coercion
  lines.push("function read(name: string): string {");
  lines.push("  return process.env[name] ?? \"\";");
  lines.push("}");
  lines.push("");
  lines.push("function toNumber(name: string): number {");
  lines.push("  return Number(process.env[name]);");
  lines.push("}");
  lines.push("");
  lines.push("function toBoolean(name: string): boolean {");
  lines.push('  const v = (process.env[name] ?? "").toLowerCase();');
  lines.push('  return v !== "false" && v !== "0" && v !== "";');
  lines.push("}");
  lines.push("");

  // Server env export
  lines.push("export const env: Env = {");
  for (const v of varsFile.variables) {
    const accessor = getAccessor(v);
    lines.push(`  ${v.name}: ${accessor},`);
  }
  lines.push("};");
  lines.push("");

  // Client env export (PUBLIC_* and NEXT_PUBLIC_* variables only)
  const clientVars = varsFile.variables.filter(
    (v) => v.name.startsWith("PUBLIC_") || v.name.startsWith("NEXT_PUBLIC_"),
  );
  if (clientVars.length > 0) {
    const pickKeys = clientVars.map((v) => `"${v.name}"`).join(" | ");
    lines.push(`export const clientEnv: Pick<Env, ${pickKeys}> = {`);
    for (const v of clientVars) {
      const accessor = getAccessor(v);
      lines.push(`  ${v.name}: ${accessor},`);
    }
    lines.push("};");
    lines.push("");
  }

  return lines.join("\n");
}

function getAccessor(variable: Variable): string {
  const schema = variable.schema;
  const name = variable.name;

  if (schema.includes("z.number()") || schema.includes("z.coerce.number()")) {
    return `toNumber("${name}")`;
  }
  if (schema.includes("z.boolean()") || schema.includes("z.coerce.boolean()")) {
    return `toBoolean("${name}")`;
  }

  // Enum fields need a cast to the literal union type
  const tsType = inferTsType(variable);
  if (tsType !== "string") {
    return `read("${name}") as ${tsType}`;
  }

  return `read("${name}")`;
}

function inferTsType(variable: Variable): string {
  const schema = variable.schema;

  // Number types
  if (schema.includes("z.number()") || schema.includes("z.coerce.number()")) {
    return "number";
  }

  // Boolean types
  if (schema.includes("z.boolean()") || schema.includes("z.coerce.boolean()")) {
    return "boolean";
  }

  // Enum types — extract literal union
  const enumMatch = schema.match(/z\.enum\(\[([^\]]+)\]\)/);
  if (enumMatch) {
    return enumMatch[1]
      .split(",")
      .map((s) => s.trim())
      .join(" | ");
  }

  // String types
  return "string";
}
